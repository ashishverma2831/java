package patternPrograms;

public class pattern10_11 {
    public static void main(String[] args) {
     for(int i=1;i<=5;i++){
        for(int j=1;j<=3;j++){      
            System.out.print(j+" "+i+" ");          //pattern 10
        }
        System.out.println();
     }   
     System.out.println("************");
     for(int i=1;i<=5;i++){
        for(int j=1;j<=3;j++){
            System.out.print(i+" "+j+" ");           //pattern 11
        }
        System.out.println();
     } 
    }
}
